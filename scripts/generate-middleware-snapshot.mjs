import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  DEFAULT_OG_IMAGE,
  OG_IMAGE_HEIGHT,
  OG_IMAGE_WIDTH,
  OG_LOCALE,
  SITE_NAME,
  SITE_URL,
  TWITTER_SITE,
  pageSeo,
} from "../src/seo.config.js";
import { buildSeoJsonLd } from "../src/seo/jsonLd.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");

function absUrl(resourcePath = "/") {
  if (/^https?:\/\//i.test(resourcePath)) {
    return resourcePath;
  }
  return `${SITE_URL}${resourcePath.startsWith("/") ? resourcePath : `/${resourcePath}`}`;
}

function routeKeyFromPath(p) {
  if (p === "/") return "/";
  return p.replace(/\/$/, "") || "/";
}

const snapshot = {};

for (const key of Object.keys(pageSeo)) {
  const p = pageSeo[key];
  const rk = routeKeyFromPath(p.path);
  const canonicalUrl = absUrl(p.path);
  const imageUrl = absUrl(DEFAULT_OG_IMAGE);
  const keywords = Array.isArray(p.keywords) ? p.keywords.join(", ") : p.keywords;
  const jsonLd = buildSeoJsonLd({
    title: p.title,
    description: p.description,
    canonicalUrl,
  });

  snapshot[rk] = {
    title: p.title,
    description: p.description,
    canonicalUrl,
    imageUrl,
    keywords,
    siteName: SITE_NAME,
    ogLocale: OG_LOCALE,
    twitterSite: TWITTER_SITE,
    ogImageWidth: OG_IMAGE_WIDTH,
    ogImageHeight: OG_IMAGE_HEIGHT,
    ogImageType: "image/png",
    jsonLdString: JSON.stringify(jsonLd).replace(/</g, "\\u003c"),
  };
}

const banner = `// Generated by scripts/generate-middleware-snapshot.mjs — do not edit.

`;

const body = `export default ${JSON.stringify(snapshot, null, 2)};
`;

await writeFile(path.join(rootDir, "middleware-snapshot.js"), banner + body, "utf8");
console.log("Wrote middleware-snapshot.js");
